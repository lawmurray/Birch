#define TERNARY(f) \
    TERNARY_FIRST(f, real) \
    TERNARY_FIRST(f, int) \
    TERNARY_FIRST(f, bool)
#define TERNARY_FIRST(f, T) \
    TERNARY_SECOND(f, T, real) \
    TERNARY_SECOND(f, T, int) \
    TERNARY_SECOND(f, T, bool)
#define TERNARY_SECOND(f, T, U) \
    TERNARY_THIRD(f, T, U, real) \
    TERNARY_THIRD(f, T, U, int) \
    TERNARY_THIRD(f, T, U, bool)
#define TERNARY_THIRD(f, T, U, V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), NUMBIRCH_ARRAY(U, 2), NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), NUMBIRCH_ARRAY(U, 1), NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 0), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), U, NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), U, V) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 0), V) \
    TERNARY_SIG(f, T, U, NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, T, U, V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), NUMBIRCH_ARRAY(U, 2), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), NUMBIRCH_ARRAY(U, 2), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), U, NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), NUMBIRCH_ARRAY(U, 0), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), U, NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 2), U, V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 2), NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 2), NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 2), V) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 2), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 2), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), U, NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, T, U, NUMBIRCH_ARRAY(V, 2)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), NUMBIRCH_ARRAY(U, 1), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), NUMBIRCH_ARRAY(U, 1), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), U, NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), NUMBIRCH_ARRAY(U, 0), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), U, NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 1), U, V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 1), NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 1), NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 1), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 1), V) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 1), NUMBIRCH_ARRAY(V, 0)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 1), V) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, NUMBIRCH_ARRAY(T, 0), U, NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, T, NUMBIRCH_ARRAY(U, 0), NUMBIRCH_ARRAY(V, 1)) \
    TERNARY_SIG(f, T, U, NUMBIRCH_ARRAY(V, 1))
#define TERNARY_SIG(f, T, U, V) \
    template real_t<T,U,V> f<T,U,V>(const T&, const U&, const V&);

